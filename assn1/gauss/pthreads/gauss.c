#define SWAP(a,b)       {double tmp; tmp = a; a = b; b = tmp;}

/* Solve the equation:
 *   matrix * X = R
 */

double **matrix, *X, *R;

/* Pre-set solution. */

double *X__;

/* Initialize pthread stuff */
pthread_mutex_t mut = PTHREAD_MUTEX_INITIALIZER;
